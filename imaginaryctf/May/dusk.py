def do_on_input(buffer, nextState: int, input : int):
    input_consumed = False
    
    while nextState <= 4095:
        # source state
        a1 = buffer[nextState]
        # target state
        a2 = buffer[nextState + 1]
        # next instruction index
        a3 = buffer[nextState + 2]

        # 0x0ffe signifies INPUT
        if a1 == 0x0ffe or a2 == 0x0ffe:
            if input_consumed:
                return nextState
            
            input_consumed = True
            c = input
            
            if a1 == 0x0ffe:
                buffer[a1] = (-c) % (1 << 64)
            if a2 == 0x0ffe:
                buffer[a2] = (-c) % (1 << 64)

        buffer[a2] = (buffer[a2] - buffer[a1]) % (1 << 64)

        # 0x0fff signifies PRINT
        if a2 == 0x0fff:
            raise ValueError("We got into a print")

        if a1 == 0x0ffe:
            buffer[0x7ff0 // 8] = 0  # offset 32752 / 8

        # if the next operation is not defined, we jump, else we go to the next one
        if buffer[a2] == 0:
            nextState = a3
        else:
            nextState += 3
    
    raise RuntimeError("Finished")

